let grade = prompt('Enter your grade.')

grade = parseInt(grade)

if (grade >= 90 && grade <= 100){
    alert('You got an A')
}
else if (grade >= 80 && grade < 90){
    alert('You got a B')
}
else if (grade >= 70 && grade < 80){
    alert('You got a C')
}
else if (grade >= 60 && grade < 70){
    alert('You got a D')
}
else{
    alert('You got a F')
}

